// Array de exemplo
var meuArray = [1, 2, 3, 4, 5];

// Verifique se o array tem pelo menos dois elementos
if (meuArray.length >= 2) {
  // Imprima o último número do array
  console.log("O último número do array é: " + meuArray[meuArray.length - 1]);
} else {
  console.log("O array não possui pelo menos dois elementos.");
}
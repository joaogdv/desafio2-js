// Array de exemplo
var meuArray = [1, 2, 3, 4, 5];

// Verifique se o array tem pelo menos um elemento e se o primeiro número é maior que 0
if (meuArray.length > 0 && meuArray[0] > 0) {
  // Imprima o primeiro número do array
  console.log("O primeiro número do array é: " + meuArray[0]);
} else {
  console.log("O array não possui pelo menos um elemento ou o primeiro número não é maior que 0.");
}